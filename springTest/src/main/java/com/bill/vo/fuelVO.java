package com.bill.vo;

public class fuelVO {
		 
	    private String time;
	    private String Fuel;
	    private String route;
	 
	    public String getTime() {
	        return time;
	    }
	 
	    public void setTime(String time) {
	        this.time = time;
	    }
	 
	    public String getFuel() {
	        return Fuel;
	    }
	 
	    public void setFuel(String Fuel) {
	        this.Fuel = Fuel;
	    }
	    
	    public String getRoute() {
	        return route;
	    }
	 
	    public void setRoute(String route) {
	        this.route = route;
	    }
	 

}
